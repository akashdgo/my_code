package main

import (
	"fmt"
)

func main() {
	// All the lines below are each a statement
	// Each statement takes up one line.

	// This is declaring x : and assigning a value.
	x := 42
	fmt.Println(x)

	// This is value assigned to the already declared varible.
	x = 77
	fmt.Println(x)

	// The int '100' & '77' are Operands and '+' is the Operator
	// Each statement is made up of one or more explicit values, constants, variables, operators, and functions
	// the programming language interprets and computes to produce another value.

	y := 100 + 77  // '100 +77' is an expression.
	fmt.Println(y) // All declared variable need to be used.
}
